import argparse
import os
import sys
from pathlib import Path

import pandas as pd


REQUIRED_COLUMNS = {
    "period",
    "symbol",
    "scenario",
    "total_return_pct",
    "max_drawdown_pct",
    "profit_factor",
    "win_rate_pct",
    "average_holding_days",
}

SUMMARY_COLUMNS = [
    "scenario",
    "periods_tested",
    "symbols_tested",
    "total_cases",
    "avg_total_return_pct",
    "avg_max_drawdown_pct",
    "avg_profit_factor",
    "avg_win_rate_pct",
    "avg_average_holding_days",
]

RANKING_COLUMNS = SUMMARY_COLUMNS + ["score"]


def parse_args() -> argparse.Namespace:
    parser = argparse.ArgumentParser(
        description="Analyze exported period experiment CSV results."
    )
    parser.add_argument(
        "--input",
        default="reports/period_2021_2025.csv",
        help="Raw period experiment CSV generated by run_period_experiment.py.",
    )
    parser.add_argument(
        "--output-dir",
        default="reports",
        help="Directory where summary CSV files and charts will be saved.",
    )
    return parser.parse_args()


def validate_input_path(input_path: Path) -> None:
    if not input_path.exists():
        print(f"Error: input file does not exist: {input_path}")
        sys.exit(1)


def validate_columns(df: pd.DataFrame) -> None:
    missing_columns = sorted(REQUIRED_COLUMNS - set(df.columns))
    if missing_columns:
        print(
            "Error: input CSV is missing required columns: "
            + ", ".join(missing_columns)
        )
        sys.exit(1)


def build_scenario_summary(df: pd.DataFrame) -> pd.DataFrame:
    success_df = df[df["scenario"] != "ERROR"].copy()
    if success_df.empty:
        return pd.DataFrame(columns=SUMMARY_COLUMNS)

    numeric_columns = [
        "total_return_pct",
        "max_drawdown_pct",
        "profit_factor",
        "win_rate_pct",
        "average_holding_days",
    ]
    for column in numeric_columns:
        success_df[column] = pd.to_numeric(success_df[column], errors="coerce")

    return (
        success_df.groupby("scenario", sort=False)
        .agg(
            periods_tested=("period", "nunique"),
            symbols_tested=("symbol", "nunique"),
            total_cases=("symbol", "count"),
            avg_total_return_pct=("total_return_pct", "mean"),
            avg_max_drawdown_pct=("max_drawdown_pct", "mean"),
            avg_profit_factor=("profit_factor", "mean"),
            avg_win_rate_pct=("win_rate_pct", "mean"),
            avg_average_holding_days=("average_holding_days", "mean"),
        )
        .reset_index()
    )


def build_scenario_ranking(summary_df: pd.DataFrame) -> pd.DataFrame:
    if summary_df.empty:
        return pd.DataFrame(columns=RANKING_COLUMNS)

    ranking_df = summary_df.copy()
    ranking_df["score"] = (
        ranking_df["avg_total_return_pct"].fillna(0)
        + ranking_df["avg_profit_factor"].fillna(0) * 2
        + ranking_df["avg_max_drawdown_pct"].fillna(0) * 0.3
    )
    ranking_df = ranking_df.sort_values("score", ascending=False).reset_index(
        drop=True
    )
    return ranking_df[RANKING_COLUMNS]


def save_chart(
    ranking_df: pd.DataFrame,
    metric_column: str,
    title: str,
    output_path: Path,
) -> None:
    import matplotlib.pyplot as plt

    plt.figure(figsize=(10, 5))
    plt.bar(ranking_df["scenario"], ranking_df[metric_column])
    plt.xlabel("scenario")
    plt.ylabel(metric_column)
    plt.title(title)
    plt.xticks(rotation=45, ha="right")
    plt.tight_layout()
    plt.savefig(output_path)
    plt.close()


def save_outputs(summary_df: pd.DataFrame, ranking_df: pd.DataFrame, output_dir: Path) -> None:
    output_dir.mkdir(parents=True, exist_ok=True)
    summary_df.to_csv(output_dir / "scenario_summary.csv", index=False)
    ranking_df.to_csv(output_dir / "scenario_ranking.csv", index=False)

    if ranking_df.empty:
        return

    matplotlib_cache_dir = output_dir / ".matplotlib_cache"
    matplotlib_cache_dir.mkdir(parents=True, exist_ok=True)
    os.environ["MPLCONFIGDIR"] = str(matplotlib_cache_dir)

    save_chart(
        ranking_df,
        "avg_total_return_pct",
        "Average Total Return by Scenario",
        output_dir / "avg_total_return_pct.png",
    )
    save_chart(
        ranking_df,
        "avg_max_drawdown_pct",
        "Average Max Drawdown by Scenario",
        output_dir / "avg_max_drawdown_pct.png",
    )
    save_chart(
        ranking_df,
        "avg_profit_factor",
        "Average Profit Factor by Scenario",
        output_dir / "avg_profit_factor.png",
    )


def print_console_summary(
    input_path: Path,
    output_dir: Path,
    summary_df: pd.DataFrame,
    ranking_df: pd.DataFrame,
) -> None:
    print("Period Result Analysis")
    print("----------------------")
    print(f"Input file: {input_path}")
    print(f"Output directory: {output_dir}")
    print(f"Scenarios analyzed: {len(summary_df)}")

    if ranking_df.empty:
        print("No successful scenario rows were available for analysis.")
    else:
        best_score = ranking_df.iloc[0]
        best_return = ranking_df.loc[ranking_df["avg_total_return_pct"].idxmax()]
        best_drawdown = ranking_df.loc[ranking_df["avg_max_drawdown_pct"].idxmax()]
        best_profit_factor = ranking_df.loc[
            ranking_df["avg_profit_factor"].idxmax()
        ]

        print(
            "Best overall scenario by score: "
            f"{best_score['scenario']} (score {best_score['score']:.2f})"
        )
        print(
            "Best average return scenario: "
            f"{best_return['scenario']} "
            f"({best_return['avg_total_return_pct']:.2f}%)"
        )
        print(
            "Best drawdown control scenario: "
            f"{best_drawdown['scenario']} "
            f"({best_drawdown['avg_max_drawdown_pct']:.2f}%)"
        )
        print(
            "Best profit factor scenario: "
            f"{best_profit_factor['scenario']} "
            f"({best_profit_factor['avg_profit_factor']:.2f})"
        )

    print()
    print("Note: This report is generated from simple rules for educational research only.")
    print("It is not financial advice.")


def main() -> None:
    args = parse_args()
    input_path = Path(args.input)
    output_dir = Path(args.output_dir)

    validate_input_path(input_path)

    try:
        raw_df = pd.read_csv(input_path)
    except Exception as exc:
        print(f"Error: failed to read input CSV: {exc}")
        sys.exit(1)

    validate_columns(raw_df)

    summary_df = build_scenario_summary(raw_df)
    ranking_df = build_scenario_ranking(summary_df)

    try:
        save_outputs(summary_df, ranking_df, output_dir)
    except Exception as exc:
        print(f"Error: failed to save analysis outputs: {exc}")
        sys.exit(1)

    print_console_summary(input_path, output_dir, summary_df, ranking_df)


if __name__ == "__main__":
    main()
